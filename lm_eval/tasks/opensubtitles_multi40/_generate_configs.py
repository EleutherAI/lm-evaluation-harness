PAIRS_SUBDIR = "pairs"
GROUPS_SUBDIR = "groups"
COMMON_TEMPLATE_FILE = "../_opensubtitles_multi40_common.yaml"
TASK_VERSION = "1"


def slug_lang(lang):
    return lang.lower().replace("-", "_")


def task_name(prefix, src, tgt):
    return "{}_{}_to_{}".format(prefix, slug_lang(src), slug_lang(tgt))


def parse_languages(args):
    if args.languages:
        langs = [s.strip() for s in args.languages.split(",") if s.strip()]
        if not langs:
            raise ValueError("--languages was provided but no valid language codes were found.")
        return langs

    if args.dataset_name:
        langs = [s.strip() for s in args.dataset_name.split("-") if s.strip()]
        if not langs:
            raise ValueError("--dataset_name was provided but parsing yielded no language codes.")
        return langs

    if args.dataset_dir:
        try:
            import datasets
            from huggingface_hub import snapshot_download
        except ImportError as exc:
            raise ImportError(
                "datasets/huggingface_hub is not installed. Either install them, or pass --languages/--dataset_name."
            ) from exc

        try:
            obj = datasets.load_from_disk(args.dataset_dir)
        except Exception:
            try:
                snapshot_path = snapshot_download(repo_id=args.dataset_dir, repo_type="dataset")
                obj = datasets.load_from_disk(snapshot_path)
            except Exception:
                obj = datasets.load_dataset(args.dataset_dir)
        if isinstance(obj, datasets.DatasetDict):
            if args.split in obj:
                dataset = obj[args.split]
            elif len(obj) == 1:
                only_split = next(iter(obj.keys()))
                print(
                    "[warn] Requested split '{}' not found; using only split '{}'".format(
                        args.split, only_split
                    )
                )
                dataset = obj[only_split]
            else:
                raise ValueError(
                    "Split '{}' not found. Available splits: {}".format(
                        args.split, list(obj.keys())
                    )
                )
        elif isinstance(obj, datasets.Dataset):
            print("[warn] Loaded Dataset (not DatasetDict); using it directly.")
            dataset = obj
        else:
            raise TypeError("Unsupported dataset type: {}".format(type(obj)))

        if "translation" in dataset.column_names:
            feat = dataset.features.get("translation")
            if hasattr(feat, "keys"):
                return sorted(list(feat.keys()))
            if len(dataset) == 0:
                raise ValueError("Dataset is empty; cannot infer language keys.")
            first = dataset[0].get("translation", {})
            if not isinstance(first, dict) or not first:
                raise ValueError("'translation' column is not a non-empty dict.")
            return sorted(first.keys())

        meta_columns = {
            "id",
            "movie_id",
            "segment_id",
            "doc_id",
            "line_number",
            "source",
            "timestamp_start",
            "timestamp_end",
            "speaker",
            "translation",
        }
        return sorted([c for c in dataset.column_names if c not in meta_columns])

    return DEFAULT_LANGUAGES


def write_direction_yaml(path, prefix, src, tgt, dataset_dir, split):
    tname = task_name(prefix, src, tgt)
    content = """# Auto-generated by _generate_configs.py
include: {common_template}
task: {task}
task_alias: opensubtitles {src}->{tgt}
tag:
  - opensubtitles_multi40
  - opensubtitles_{src_slug}_to_xx
  - opensubtitles_src_{src_slug}
  - opensubtitles_tgt_{tgt_slug}
metadata:
  version: {version}
  dataset_dir: "{dataset_dir}"
  split: "{split}"
  src_lang: "{src}"
  tgt_lang: "{tgt}"
""".format(
        common_template=COMMON_TEMPLATE_FILE,
        task=tname,
        src=src,
        tgt=tgt,
        src_slug=slug_lang(src),
        tgt_slug=slug_lang(tgt),
        version=TASK_VERSION,
        dataset_dir=dataset_dir,
        split=split,
    )
    path.write_text(content, encoding="utf-8")
    return tname


def write_group_yaml(path, group_name, tasks, group_alias):
    task_block = "\n".join(["  - {}".format(task) for task in tasks])
    content = """# Auto-generated by _generate_configs.py
group: {group_name}
group_alias: {group_alias}
task:
{task_block}
aggregate_metric_list:
  - metric: bleu
    aggregation: mean
    weight_by_size: true
  - metric: chrf
    aggregation: mean
    weight_by_size: true
metadata:
  version: {version}
""".format(
        group_name=group_name,
        group_alias=group_alias,
        task_block=task_block,
        version=TASK_VERSION,
    )
    path.write_text(content, encoding="utf-8")


def parse_args():
    parser = argparse.ArgumentParser(description="Generate OpenSubtitles multi40 task YAMLs.")
    parser.add_argument(
        "--dataset_dir",
        type=str,
        default="Helsinki-NLP/OpenSubtitles2024-40-langs-15-movies",
        help=(
            "Local save_to_disk dataset path OR HF dataset id. "
            "Used for metadata and optional language inference."
        ),
    )
    parser.add_argument(
        "--split",
        type=str,
        default="devtest",
        help="Split to read from the dataset_dir.",
    )
    parser.add_argument(
        "--src_lang",
        type=str,
        default="en",
        help="Source language for en->xx style task generation.",
    )
    parser.add_argument(
        "--src_langs",
        type=str,
        default=None,
        help="Comma-separated source languages (overrides --src_lang). Example: en,zh_CN,fi",
    )
    parser.add_argument(
        "--all_sources",
        action="store_true",
        help="Use all detected languages as source languages (generate all directed xx->yy pairs).",
    )
    parser.add_argument(
        "--prefix",
        type=str,
        default="opensubtitles_multi40",
        help="Task name prefix.",
    )
    parser.add_argument(
        "--languages",
        type=str,
        default=None,
        help="Comma-separated language list. If provided, no dataset loading is needed.",
    )
    parser.add_argument(
        "--dataset_name",
        type=str,
        default=None,
        help="Hyphen-separated dataset name like ar-bg-...-zh_TW; used to infer languages.",
    )
    parser.add_argument(
        "--write_all_pairs_group",
        action="store_true",
        help="When multiple sources are generated, also write an all-pairs group yaml.",
    )
    parser.add_argument(
        "--write_target_groups",
        action="store_true",
        help="Also write target-centric groups named xx_<lang>.yaml (xx->lang).",
    )
    parser.add_argument(
        "--core_en_bidirectional",
        action="store_true",
        help=(
            "Generate only the core benchmark directions en->xx and xx->en, "
            "and write both group YAMLs: en_xx.yaml and xx_en.yaml."
        ),
    )
    return parser.parse_args()


def resolve_sources(args, languages):
    if args.all_sources:
        return list(languages)

    if args.src_langs:
        srcs = [s.strip() for s in args.src_langs.split(",") if s.strip()]
        if not srcs:
            raise ValueError("--src_langs was provided but no valid source languages were found.")
        return srcs

    return [args.src_lang]


def main():
    args = parse_args()
    out_dir = Path(__file__).resolve().parent
    pairs_dir = out_dir / PAIRS_SUBDIR
    groups_dir = out_dir / GROUPS_SUBDIR
    pairs_dir.mkdir(parents=True, exist_ok=True)
    groups_dir.mkdir(parents=True, exist_ok=True)

    languages = parse_languages(args)
    sources = resolve_sources(args, languages)
    invalid_sources = [src for src in sources if src not in languages]
    if invalid_sources:
        raise ValueError(
            "Source language(s) not found in detected languages: {}. Available: {}".format(
                invalid_sources, languages
            )
        )

    generated_total = 0
    all_generated_tasks = []
    tasks_by_tgt = {}

    if args.core_en_bidirectional:
        if "en" not in languages:
            raise ValueError("`--core_en_bidirectional` requires 'en' to be in detected languages.")

        en_to_xx_tasks = []
        xx_to_en_tasks = []
        non_en_languages = [lang for lang in languages if lang != "en"]

        for tgt_lang in non_en_languages:
            task_file = pairs_dir / "en_to_{}.yaml".format(slug_lang(tgt_lang))
            tname = write_direction_yaml(
                task_file,
                prefix=args.prefix,
                src="en",
                tgt=tgt_lang,
                dataset_dir=args.dataset_dir,
                split=args.split,
            )
            en_to_xx_tasks.append(tname)

        for src_lang in non_en_languages:
            task_file = pairs_dir / "{}_to_en.yaml".format(slug_lang(src_lang))
            tname = write_direction_yaml(
                task_file,
                prefix=args.prefix,
                src=src_lang,
                tgt="en",
                dataset_dir=args.dataset_dir,
                split=args.split,
            )
            xx_to_en_tasks.append(tname)

        write_group_yaml(
            groups_dir / "en_xx.yaml",
            "{}_en_xx".format(args.prefix),
            en_to_xx_tasks,
            "opensubtitles en->xx",
        )
        write_group_yaml(
            groups_dir / "xx_en.yaml",
            "{}_xx_en".format(args.prefix),
            xx_to_en_tasks,
            "opensubtitles xx->en",
        )

        generated_total = len(en_to_xx_tasks) + len(xx_to_en_tasks)
        print("Detected languages ({}): {}".format(len(languages), ", ".join(languages)))
        print("Core benchmark mode enabled: en<->xx")
        print("Generated {} translation task yaml files.".format(generated_total))
        print("Group tasks: {}_en_xx, {}_xx_en".format(args.prefix, args.prefix))
        return

    for src_lang in sources:
        targets = [lang for lang in languages if lang != src_lang]
        generated_tasks = []

        for tgt_lang in targets:
            file_name = "{}_to_{}.yaml".format(slug_lang(src_lang), slug_lang(tgt_lang))
            task_file = pairs_dir / file_name
            tname = write_direction_yaml(
                task_file,
                prefix=args.prefix,
                src=src_lang,
                tgt=tgt_lang,
                dataset_dir=args.dataset_dir,
                split=args.split,
            )
            generated_tasks.append(tname)
            tasks_by_tgt.setdefault(tgt_lang, []).append(tname)

        group_name = "{}_{}_xx".format(args.prefix, slug_lang(src_lang))
        group_file = groups_dir / "{}_xx.yaml".format(slug_lang(src_lang))
        write_group_yaml(
            group_file,
            group_name,
            generated_tasks,
            "opensubtitles {}->xx".format(src_lang),
        )

        generated_total += len(generated_tasks)
        all_generated_tasks.extend(generated_tasks)

    write_target_groups = args.write_target_groups or args.all_sources
    if write_target_groups:
        for tgt_lang, tgt_tasks in sorted(tasks_by_tgt.items(), key=lambda x: slug_lang(x[0])):
            group_name = "{}_xx_{}".format(args.prefix, slug_lang(tgt_lang))
            group_file = groups_dir / "xx_{}.yaml".format(slug_lang(tgt_lang))
            write_group_yaml(
                group_file,
                group_name,
                tgt_tasks,
                "opensubtitles xx->{}".format(tgt_lang),
            )

    if args.write_all_pairs_group and len(sources) > 1:
        all_pairs_group_name = "{}_all_pairs".format(args.prefix)
        all_pairs_group_file = groups_dir / "all_pairs.yaml"
        write_group_yaml(
            all_pairs_group_file,
            all_pairs_group_name,
            all_generated_tasks,
            "opensubtitles all->all",
        )

    print("Detected languages ({}): {}".format(len(languages), ", ".join(languages)))
    print("Sources used ({}): {}".format(len(sources), ", ".join(sources)))
    print("Generated {} translation task yaml files.".format(generated_total))
    if len(sources) == 1:
        print(
            "Group task: {}_{}_xx".format(
                args.prefix,
                slug_lang(sources[0]),
            )
        )
    else:
        print("Per-source group tasks generated: <src>_xx.yaml")
        if write_target_groups:
            print("Per-target group tasks generated: xx_<tgt>.yaml")
        if args.write_all_pairs_group:
            print("All-pairs group task: {}_all_pairs".format(args.prefix))


if __name__ == "__main__":
    main()
